package com.fable.kscc.api.beans.component;

public class Satellite {

	private String multicast_addr;
	private int port;
	private int return_num;

	public String getMulticast_addr() {
		return multicast_addr;
	}

	public void setMulticast_addr(String multicast_addr) {
		this.multicast_addr = multicast_addr;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}

	public int getReturn_num() {
		return return_num;
	}

	public void setReturn_num(int return_num) {
		this.return_num = return_num;
	}

}
